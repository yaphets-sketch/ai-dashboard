def main():
    with open(UNIVERSE_FILE, "r", encoding="utf-8") as f:
        data = json.load(f)

    count = 0
    for stock in data["stocks"]:
        code = stock["code"]
        if code in EXTRA:
            stock["concepts"] = [{"name": n, "verified": v, "detail": ""} for n, v in EXTRA[code]]
            count += 1
        elif not stock.get("concepts"):
            stock["concepts"] = [{"name": "AI相关", "verified": None, "detail": "待校验"}]
            count += 1

    with open(UNIVERSE_FILE, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    with_c = sum(1 for s in data["stocks"] if s.get("concepts"))
    print(f"Concepts: {with_c}/{data['total']} = {with_c*100//275}%")
    print(f"This batch: {count}")

if __name__ == "__main__":
    main()
