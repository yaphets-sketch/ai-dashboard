def main():
    with open(UNIVERSE_FILE, 'r', encoding='utf-8') as f:
        data = json.load(f)

    updated = 0
    for stock in data['stocks']:
        code = stock['code']
        if code in WEB_NAMES:
            if stock.get('name') == code or stock['name'] == '' or stock['name'] == stock['code']:
                stock['name'] = WEB_NAMES[code]
                updated += 1

    data['updated'] = "2026-06-13T18:00:00+08:00"

    with open(UNIVERSE_FILE, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    unnamed = sum(1 for s in data['stocks'] if s['name'] == s['code'] or s['name'] == '')

    # Category stats
    from collections import Counter
    cats = Counter(s['category'] for s in data['stocks'])
    print(f"Updated: {updated} names")
    print(f"Still unnamed: {unnamed}")
    print(f"Total: {data['total']}")
    print(f"Categories: {dict(cats)}")

if __name__ == '__main__':
    main()
