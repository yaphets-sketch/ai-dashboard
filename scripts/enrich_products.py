def main():
    with open(UNIVERSE_FILE, "r", encoding="utf-8") as f:
        data = json.load(f)

    updated = 0
    for stock in data["stocks"]:
        code = stock["code"]
        if code in ENRICH:
            enrich = ENRICH[code]
            # Merge with existing deep analysis
            da = stock.get("deep_analysis", {})
            # New products with revenue + customer info
            new_prods = []
            for p in enrich["products"]:
                name, status, detail, customers, extra = p[0], p[1], p[2], p[3] if len(p)>3 else "", p[4] if len(p)>4 else ""
                d = f"{detail}. 客户: {customers}. {extra}" if customers else detail
                new_prods.append({"name": name, "status": status, "detail": d})
            da["products"] = new_prods
            stock["deep_analysis"] = da
            updated += 1

    with open(UNIVERSE_FILE, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"Enriched: {updated} stocks with detailed product matrix")

if __name__ == "__main__":
    main()
