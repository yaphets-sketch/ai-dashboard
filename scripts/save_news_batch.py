# 新闻解释模板：根据标题关键词生成简读
def interpret(title, content):
    """对新闻做一句话 AI 解读"""
    t = title + content
    interpretations = []

    if 'H股' in title and '上市' in title:
        interpretations.append("【解读】筹划港股上市，拓宽融资渠道，利好长期发展，但短期有摊薄风险")
    if '减持' in title:
        interpretations.append("【解读】高管/股东减持，短期情绪偏空，但量不大需关注后续")
    if '一季报' in title and '净利润' in title:
        if '增长' in content or '上涨' in content:
            interpretations.append("【解读】一季报业绩增长，基本面持续向好")
        else:
            interpretations.append("【解读】一季报已披露，关注营收和利润趋势")
    if '涨停' in title or '大涨' in title:
        interpretations.append("【解读】短期受资金追捧，需关注是否有基本面支撑")
    if '跳水' in title or '大跌' in title:
        interpretations.append("【解读】短期回调，关注成交量和后续走势")
    if '董事长' in title and ('选举' in title or '换届' in title):
        interpretations.append("【解读】管理层换届属于常规事项，关注新团队战略方向")
    if '1260H' in title or '国防部' in title:
        interpretations.append("【解读】被列入清单属外部风险，公司否认并表示经营未受影响，需持续关注")
    if '资金流出' in title:
        interpretations.append("【解读】主力资金短期出逃，注意回调风险")
    if '突破' in title and ('亿' in title or '市值' in title):
        interpretations.append("【解读】市值突破重要关口，市场认可度提升")
    if '成立' in title and '公司' in title:
        interpretations.append("【解读】新设子公司，通常在布局新业务方向")
    if '年报' in title and '净利润' in title:
        interpretations.append("【解读】年报已出，关注全年业绩趋势和分红方案")
    if '实际控制人' in title or '一致行动' in title:
        interpretations.append("【解读】实控人协议调整，关注是否涉及控制权变更，公司声明不影响")
    if '订单' in title or '中标' in title or '合同' in title:
        interpretations.append("【解读】新订单/合同落地，利好营收增长")

    if not interpretations:
        # 默认解读
        if '资金' in t:
            interpretations.append("【解读】资金面动态，需结合整体市场环境判断")
        elif '涨' in t or '跌' in t:
            interpretations.append("【解读】市场波动信息，关注后续走势")
        else:
            interpretations.append("")

    return ' | '.join(interpretations)


# 批量数据：(code, mcp_json_string)
NEWS_DATA = {}

# 手动从 MCP 结果提取并存储
def save_one(code, items):
    enriched = []
    for n in items:
        title = n.get("新闻标题", "")
        content = n.get("新闻内容", "")
        interp = interpret(title, content)
        enriched.append({
            "title": title,
            "content": content[:300] if content else "",
            "date": n.get("发布时间", ""),
            "source": n.get("文章来源", ""),
            "link": n.get("新闻链接", ""),
            "interpretation": interp,
        })

    cache = {
        "code": code,
        "updated": "2026-06-13T18:00:00+08:00",
        "count": len(enriched),
        "items": enriched[:8],  # Top 8
    }
    filepath = os.path.join(NEWS_DIR, f"{code}.json")
    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(cache, f, ensure_ascii=False, indent=2)
    print(f"Saved {len(enriched[:8])} news for {code}")
    return cache

